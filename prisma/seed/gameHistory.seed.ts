  const lines = gameHistoryData.trim().split('\n');
  const parsedData: Array<{
    date: string;
    first: string;
    second: string;
    third: string;
    drawTime: 'MID' | 'EVE';
    result: string;
  }> = [];

  for (const line of lines) {
    if (!line.trim()) continue;
    
    const parts = line.trim().split(/\s+/);
    if (parts.length >= 6) {
      const [date, first, second, third, drawTime, result] = parts;
      parsedData.push({
        date,
        first,
        second,
        third,
        drawTime: drawTime.toUpperCase() as 'MID' | 'EVE',
        result,
      });
    }
  }

  return parsedData;
